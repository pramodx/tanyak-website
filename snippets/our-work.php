    <p>
        <img class="fl-right full" src="./../img/image6.jpeg" alt="">
        The work of TANYAK can be explained by <strong>RISE</strong> – Action for Resilient and Inclusive Social
        Empowerment
        R – <strong>Resilient</strong>


        The Resilience program will focus on achievement of <strong>SDGs</strong> (Sustainable Development Goals) while
        reducing disaster risks as per <strong>SFDRR</strong> (Sendai Framework for disaster risk reduction) targets and
        supporting CCA (climate change adaptation) while reducing emissions and promoting green
        communities, infrastructure, and technologies.
    </p>
    <p>
        ISE – <strong>Inclusive Social Empowerment</strong>
        Inclusion program will focus on ensuring inclusion of excluded, marginalised, most vulnerable groups
        in mainstream development and promoting access rights and entitlements and through this
        empower their participation, voice, ownership in emerging policies, technologies and development
    </p>
